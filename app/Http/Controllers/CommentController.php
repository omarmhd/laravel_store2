        $request->validate([
            'comment' => 'required|string',
            'product_id' => 'required|integer',
        ],[
            'required'=>'هذ الحقل مطلوب',
            'comment.string'=>'يجب ان يكون نص',
            'product_id.interger'=>'هذا الحقل يجب ان يحتوي على ارقام فقط',
        ]);
            $comment=new Comment();
